public class s_w_itch {
    public static void main(String[] args) {
        int size = 2;
        switch (size){
            case 1:
                System.out.println("Small");
                break;
            case 2:
                System.out.println("Medium");
                break;
            case 3:
                System.out.println("Large \n");
                break;
        }

        String name = "BBB";
        switch (name){
            case "AAA":
                System.out.println("Letter is a");
                break;

            case "BBB":
                System.out.println("Letter is b");
                break;

            case "CCC":
                System.out.println("Letter is c \n");
                break;

        }

    }
}
