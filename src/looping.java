public class looping {
    public static void main(String[] args) {

        for(int i = 0 ; i <11 ; i ++) {
            System.out.println("HELLO WORlD");
        }

        int i = 0;
        while(i < 5){
            System.out.println("HIIIIIIIIIII");
            i++;
        }
        int c=0;

        do{
            System.out.println("MINGALABAR" + c);
            c++;
        } while (c<3);

        int k =0;
        while(k<5){
            System.out.println("Hello" + k);
            k++;
        }
    }
}
